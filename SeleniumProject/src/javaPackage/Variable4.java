package javaPackage;

public class Variable4 {

	public static void main(String[] args) {

		int c; // declaring a local variable
		c = 23; // defining a local variable-(int type.)
		float h = c; // defining a local variable -(float type.)
		System.out.println(c); // calling 'c' variable
		System.out.println(h); // calling 'h' variable
	}
}
