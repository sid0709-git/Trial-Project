package javaPackage;

public class ArithmaticOperator {
	
	public static void main(String[] args) {
		
		int h = 120;  // local variable
		int v = 12;  // local variable
		
    System.out.println(h+v);        // 132
    System.out.println(h-v);        // 108
    System.out.println(h*v);        // 1440
    System.out.println(h/v);        // 10
		
	}
}
